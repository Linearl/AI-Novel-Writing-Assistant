      "",
      "## 核心原理",
      "（1-2 段详细阐述。要求：)",
      "- 用 2-4 句话点明技法的核心机制——它为什么有效，读者心理/语言机制层面的原因",
      "- 用一个生动的比喻或类比让概念具象化（如「像建筑的承重墙」「大脑天生厌恶空白」）",
      "- 与相近技法做简短对比以划定边界（如「与排比的层层叠加不同，对偶的力量在于……」）",
      "- 语言要有文学性，不是干巴巴的定义，而是读起来像一篇精炼的散文片段",
      "",
      "## 使用方法",
